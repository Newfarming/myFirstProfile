def make_feed_expire(user, pkg):
    user_feeds = UserFeed.objects.select_related(
        'feed',
    ).filter(
        user_charge_pkg=pkg,
        user=user,
        is_deleted=False,
    )

    for user_feed in user_feeds:
        feed = user_feed.feed
        user_feed.expire_time = EXPIRE_DATE
        feed.expire_time = EXPIRE_DATE

        feed_oid = get_oid(feed.feed_obj_id)
        mongo_feed = Feed2.objects.filter(
            id=feed_oid
        ).first()
        if mongo_feed:
            mongo_feed.expire_time = EXPIRE_DATE
            UserFeed2.objects.filter(
                feed=mongo_feed
            ).update(
                set__expire_time=EXPIRE_DATE
            )
            mongo_feed.save()
        user_feed.save()
        feed.save()


